"""Template Registry — Phase 11a (Project Creation Scaffolding).

Pure, import-safe definitions of the 10 built-in project templates OSA can
scaffold, plus the helpers that render starter files, ``pyproject.toml`` and
``app.json`` for a new project.

This module is intentionally side-effect free:
    * NO database imports
    * NO filesystem access on import (or anywhere in this module)
    * NO network calls

The orchestration layer (``project_manager.py``) is responsible for actually
writing files to disk, creating venvs and touching the database. This module
only *produces content*.

app.json schema contract
------------------------
The output of :func:`generate_app_json` must match what
``core/app_registry.py::_parse_app_json`` reads. In particular, runtime info
for runnable web/server apps lives under a nested ``web`` block::

    {
        "id": "my-project",
        "name": "My Project",
        "description": "...",
        "icon": "🚀",
        "category": "python",
        "type": "web",             # "web" | "cli" | "other" | ...
        "version": "0.1.0",
        "tags": [...],
        "scripts": [],
        "agent": {...} | null,
        "web": {                    # only for runnable web/server templates
            "command": ["uvicorn", "src.main:app", "--host", "0.0.0.0", "--port", "5200"],
            "port": 5200,
            "venv": ".venv"        # python templates only
        }
    }

``expected_port`` is read by the registry from ``web.port`` — there is NO
top-level ``port`` key.

Templating
----------
File-content templates use ``{{PLACEHOLDER}}`` tokens (NOT ``str.format``),
because the file bodies contain literal ``{`` / ``}`` braces (JSON, JS, JSX)
that would break ``str.format``. Use :func:`render` to substitute tokens.

Supported placeholders: ``PROJECT_NAME``, ``DESCRIPTION``, ``YEAR``, ``PORT``,
``PROJECT_TITLE``.
"""
