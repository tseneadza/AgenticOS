# ── rendering helpers ────────────────────────────────────────────────────────

def render(text: str, **ctx: str) -> str:
    """Substitute ``{{KEY}}`` tokens in *text* with values from *ctx*.

    Uses plain ``str.replace`` per key so literal ``{`` / ``}`` braces in the
    template body (JSON, JS, JSX) are left untouched. Unknown placeholders in
    *text* are simply left as-is; callers should pass every supported key.
    """
    result = text
    for key, value in ctx.items():
        result = result.replace("{{" + key + "}}", str(value))
    return result


def _title_case(project_name: str) -> str:
    """Turn ``my-cool-project`` into ``My Cool Project``."""
    return project_name.replace("-", " ").replace("_", " ").title()


