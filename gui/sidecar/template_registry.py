# ── public API ────────────────────────────────────────────────────────────────

def get_template(name: str) -> dict:
    """Return the template definition for *name*.

    Raises:
        ValueError: if *name* is not a known template id.
    """
    if name not in TEMPLATES:
        raise ValueError(
            f"Unknown template: {name!r}. Valid templates: {sorted(TEMPLATES)}"
        )
    return TEMPLATES[name]


def generate_pyproject_toml(
    project_name: str,
    description: str,
    dependencies: list[str],
    dev_dependencies: list[str] | None = None,
) -> str:
    """Render a valid UV-compatible ``pyproject.toml``.

    Dependency entries are emitted as bare, valid PEP 508 requirement strings —
    just the package name (e.g. ``"fastapi"``). We deliberately do NOT append a
    dangling version operator like ``"fastapi>="`` (which is invalid PEP 508 and
    breaks ``uv``); ``uv`` will resolve the latest compatible version and pin it
    in ``uv.lock``.
    """
    if dev_dependencies is None:
        dev_dependencies = DEFAULT_DEV_DEPENDENCIES

    desc = description or f"A Codehome project ({project_name})"

    def _fmt(items: list[str]) -> str:
        if not items:
            return ""
        return "\n" + "\n".join(f'    "{item}",' for item in items) + "\n"

    deps_block = _fmt(dependencies)
    dev_block = _fmt(dev_dependencies)

    return (
        "[project]\n"
        f'name = "{project_name}"\n'
        'version = "0.1.0"\n'
        f'description = "{desc}"\n'
        'requires-python = ">=3.10"\n'
        f"dependencies = [{deps_block}]\n"
        "\n"
        "[build-system]\n"
        'requires = ["hatchling"]\n'
        'build-backend = "hatchling.build"\n'
        "\n"
        "[tool.uv]\n"
        f"dev-dependencies = [{dev_block}]\n"
    )


def generate_app_json(
    project_name: str,
    template: str,
    port: int,
    description: str,
) -> dict:
    """Build an ``app.json`` dict matching the app_registry schema.

    Web/server + frontend templates get a nested ``web`` block with the resolved
    run command (with the real *port* substituted), ``port`` and — for python
    templates — ``venv=".venv"``. CLI templates use ``type="cli"`` and no web
    block. ``monorepo`` is minimal with ``type="other"``.
    """
    tpl = get_template(template)
    desc = description or f"A {template} project"

    app: dict = {
        "id": project_name,
        "name": _title_case(project_name),
        "description": desc,
        "icon": tpl.get("icon", "📦"),
        "category": tpl.get("category", "other"),
        "type": tpl.get("type", "web"),
        "version": "0.1.0",
        "tags": [template],
        "scripts": [],
        "agent": tpl.get("agent"),
    }

    web_command_tpl = tpl.get("web_command")
    tpl_type = tpl.get("type", "web")

    # Only runnable web services get a web block: a resolved command + port.
    if web_command_tpl and tpl_type not in ("cli", "other"):
        command = [render(part, PORT=str(port)) for part in web_command_tpl]
        web: dict = {"command": command, "port": port}
        # venv only makes sense for python-based runnable templates.
        if template in PYTHON_TEMPLATES or tpl.get("package_manager") == "uv":
            web["venv"] = ".venv"
        app["web"] = web

    return app


def generate_files(
    template_name: str,
    project_name: str,
    description: str,
    port: int,
) -> dict[str, str]:
    """Render every file for *template_name* into ``{relative_path: content}``.

    Steps:
        1. Render each ``files`` entry via :func:`render`, substituting all
           supported placeholders.
        2. For python templates, (re)generate ``pyproject.toml`` from
           :func:`generate_pyproject_toml`. For ``fullstack``, the backend
           ``backend/pyproject.toml`` is generated instead.
        3. Add ``app.json`` as ``json.dumps(generate_app_json(...), indent=2)``.

    No ``{{...}}`` tokens remain in the returned content.
    """
    tpl = get_template(template_name)
    desc = description or "A Codehome project"
    ctx = {
        "PROJECT_NAME": project_name,
        "PROJECT_TITLE": _title_case(project_name),
        "DESCRIPTION": desc,
        "YEAR": DEFAULT_YEAR,
        "PORT": str(port),
    }

    files: dict[str, str] = {}
    for rel_path, content in tpl["files"].items():
        files[rel_path] = render(content, **ctx)

    # (Re)generate pyproject.toml for python-based templates.
    pyproject = generate_pyproject_toml(
        project_name=project_name,
        description=desc,
        dependencies=tpl.get("dependencies", []),
        dev_dependencies=tpl.get("dev_dependencies"),
    )
    if template_name in PYTHON_TEMPLATES:
        files["pyproject.toml"] = pyproject
    elif template_name == "fullstack":
        # Mixed template: python lives under backend/.
        files["backend/pyproject.toml"] = generate_pyproject_toml(
            project_name=f"{project_name}-backend",
            description=desc,
            dependencies=tpl.get("dependencies", []),
            dev_dependencies=tpl.get("dev_dependencies"),
        )

    # app.json (canonical registry metadata).
    files["app.json"] = json.dumps(
        generate_app_json(project_name, template_name, port, description),
        indent=2,
    )

    return files
